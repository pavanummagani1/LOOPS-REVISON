// WHILE LOOP
/**
 * SYNTAX:
 * Decalaration
 * while(condition){
 * //block of code
 * Updation
 * }
 * 
 */

// Print the first 10 natural numbers
let i =1;
while(i<=10){
    console.log(i)
    i++;
}

